let iceCreams = ['chocolate', 'vanilla', 'strawberry'];

//push method
let pushMethod = iceCreams.push('cookies and cream');
console.log(iceCreams, pushMethod);

let popMethod = iceCreams.pop();
console.log(iceCreams, popMethod);

let unshiftMethod = iceCreams.unshift('rocky road');
console.log(iceCreams, unshiftMethod);

let shiftMethod = iceCreams.shift();
console.log(iceCreams, shiftMethod);
